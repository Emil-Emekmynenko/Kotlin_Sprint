package lesson3

fun main() {
    val firstName = "Татьяна"
    var lastName = "Андреева"
    val surname = "Сергеевна"
    var age = 20

    println("Пока ещё молодая и подающая надежды: $lastName $firstName $surname, $age лет.")

    lastName = "Сидорова"
    age = 22

    println("Замужняя, но всё ещё подающая надежды: $lastName $firstName $surname, $age года.")
}